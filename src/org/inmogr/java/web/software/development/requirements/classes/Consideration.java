package org.inmogr.java.web.software.development.requirements.classes;

public class Consideration {

	private String conDescription;
	private String conPurpose;
	private String conConstraint;

	public String getConDescription() {
		return conDescription;
	}

	public void setConDescription(String conDescription) {
		this.conDescription = conDescription;
	}

	public String getConPurpose() {
		return conPurpose;
	}

	public void setConPurpose(String conPurpose) {
		this.conPurpose = conPurpose;
	}

	public String getConConstraint() {
		return conConstraint;
	}

	public void setConConstraint(String conConstraint) {
		this.conConstraint = conConstraint;
	}

}
